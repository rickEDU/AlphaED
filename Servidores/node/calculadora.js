export function soma(a, b){
    let A = parseInt(a)
    let B = parseInt(b)
    return A+B;
}
export function subtrair(a, b){
    let A = parseInt(a)
    let B = parseInt(b)
    return A-B;
}
export function multiplicar(a, b){
    let A = parseInt(a)
    let B = parseInt(b)
    return A*B;
}
export function dividir(a, b){
    let A = parseInt(a)
    let B = parseInt(b)
    return A/B;
}

